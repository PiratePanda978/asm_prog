.MODEL SMALL
.STACK 100H
.DATA
ARR DB 7,9,34,45,66
MSG1 DB 'The sum of odd elements of array is : $'
MSG2 DB 'The average of odd elements of array is : $'
SUM DB 0
AVG DW ?
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA SI,ARR
MOV CL,0
MOV BL,2

LOOP1:
CMP CL,5
JGE ENDLOOP
MOV AL,[SI]
DIV BL
CMP AH,0
JNE ODD1
INC SI
INC CL
JMP LOOP1
ODD1:
PUSH BX
MOV BL,[SI]
ADD [SUM],BL
INC SI
INC CL
POP BX
JMP LOOP1

ENDLOOP:
LEA DX,MSG1
MOV AH,9
INT 21H
MOV AL,[SUM]
AAM
ADD AX,3030H
MOV DL,AH
MOV DH,AL
MOV AH,2
INT 21H
MOV DL,DH
INT 21H

CALL NEWLINE

LEA DX,MSG2
MOV AH,9
INT 21H

MOV AL,[SUM]
AAM
ADD AX,3030H
MOV BX,0005H
DIV BX
MOV DL,AH
MOV DH,AL
MOV AH,2
INT 21H
MOV DL,DH
INT 21H


MOV AH,4CH
INT 21H
MAIN ENDP

NEWLINE PROC
PUSH AX
PUSH DX
MOV AH,2
MOV DL,10
INT 21H
MOV DL,13
INT 21H
POP DX
POP AX
RET
NEWLINE ENDP

END MAIN